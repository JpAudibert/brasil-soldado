    private void submit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit1ActionPerformed
        Warning warning = new Warning();
        boolean response = false;
        if (Validacao.notNull(this.title.getText()) && Validacao.notNull(this.message.getText())) {
            warning.setTitle(this.title.getText());
            warning.setMessage(this.message.getText());
            if (id == 0) {
                response = this.warningController.store(warning);
            } else {
                response = this.warningController.update(warning, id);
            }

            if (response) {
                JOptionPane.showMessageDialog(null, "Registro Salvo com sucesso!");
                this.title.setText("");
                this.message.setText("");

                this.title.requestFocus();

                id = 0;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campos obrigatórios.");
        };
    }//GEN-LAST:event_submit1ActionPerformed

